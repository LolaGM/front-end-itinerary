/*
Este método nos permite invertir orden de la lista pero no ordena por valor y además retorna el arreglo de la invertido.
*/
let arreglo = [1,2,3,4,5];
arreglo.reverse();
console.log(arreglo); // output [5, 4, 3, 2, 1]

let arregloDesordenado = [5,8,4,9];
arregloDesordenado.reverse();
console.log(arregloDesordenado);
//(4) [9, 4, 8, 5]

